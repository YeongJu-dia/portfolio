$(document).ready(function(){
     
  $("nav > ul > li").mouseenter(function(){
      $(".sub_menu").stop().slideDown(300);
  });
  $("nav > ul > li").mouseleave(function(){
      $(".sub_menu").stop().slideUp(300);
  });
});



















